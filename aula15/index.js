const dataAtual = new Date();

console.log(dataAtual) // retorna a data atual
console.log(dataAtual.getDate()) // retorna o dia do mês
console.log(dataAtual.getMonth()+1) // retorna o mês
console.log(dataAtual.getFullYear()) // retorna o ano de 4 dígitos
console.log(dataAtual.getHours()) // retorna a hora
console.log(dataAtual.getMinutes()) // retorna os minutos
console.log(dataAtual.getSeconds()) // retorna os segundos
console.log(dataAtual.toUTCString()) // retorna pra UTC
console.log(dataAtual.toISOString()) // converte pra ISO







